function todaysArtifact() {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const dayOfYear = Math.floor((now - start) / 86400000);
  return window.ARTIFACTS[dayOfYear % window.ARTIFACTS.length];
}
